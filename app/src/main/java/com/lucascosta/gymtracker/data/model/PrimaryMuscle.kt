package com.lucascosta.gymtracker.data.model

/**
 * Enumeração que representa os músculos principais trabalhados durante os exercícios.
 *
 * Cada valor dessa enumeração corresponde a um grupo muscular que pode ser o foco de um exercício.
 */
enum class PrimaryMuscle {
    ABDOMEN,
    FOREARMS,
    BICEPS,
    BACK,
    GLUTES,
    SHOULDERS,
    CHEST,
    LEGS,
    TRAPEZIUS,
    TRICEPS
}
